#!/usr/bin/python3.5

# Single quotes
print ('Hello World !');

# Use \' to escape the single quote in single quote
print ('It doesn\'t contain')

# Else use double quote to supress
print ("It doesn't contain")

# Use \" to escape the double quote
print ("\"Yes\", We are")

# Else use single quote supress it
print ('"Yes", we are')

# With print(), \n produces a new line
string = 'First line \nSecond line'
print(string)


