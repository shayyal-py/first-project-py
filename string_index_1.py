#!/usr/bin/python3.5

# Strings can be indexed (subscripted), with the first character having index 0

str = 'Python'
print ("Indexing string 'Python' and value of [0] is : ", str[0])
print ("Indexing string 'Python' and value of [2] is : ", 'Python'[2])
print ("Indexing string 'Python' and value of [5] is : ", 'Python'[5])

